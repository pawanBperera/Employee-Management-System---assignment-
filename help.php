<h1>Welcome to StrategicArmorySolutions Task Management Help Center</h1>

 <h3>Introduction
Welcome to the help center for StrategicArmorySolutions Task Management. This page aims to guide you through the functionalities and features available to different user roles within our system.
</h3>

<p class="p">
 User Roles
1. Employee: Upon logging in, employees can view the tasks assigned to them. They have access to their task list and related details. <br>
2. Supervisor: Supervisors have broader capabilities. They can assign activities, generate reports, and assign tasks to employees within the system.<br>
3. Manager: Managers possess the highest level of access. They can perform supervisory functions, assign tasks, and manage employees within the system.<br>
<br><br><br>
 Functionality Overview<br>
 Employee<br>
- View Assigned Tasks: Employees can access their assigned tasks upon logging in. They can view task details such as descriptions, deadlines, and any associated files or instructions.
<br><br>
 Supervisor<br>
- Assign Activities: Supervisors have the authority to assign activities to employees. They can also generate reports to monitor task progress and completion.
<br><br>
    Manager<br>
- Assign Tasks & Employees: Managers hold comprehensive control. In addition to supervisory functions, they can assign tasks and manage employees within the system.
<br><br>
 How to Use<br>
 Logging In<br>
- To access the system, click on the "Log In" button on the homepage. Enter your credentials to proceed to your respective dashboard based on your role.
<br><br>
 Navigation<br>
- Use the respective buttons or links assigned to your role (Employee, Supervisor, Manager) to access specific functionalities such as task viewing, activity assignment, report generation, etc.
<br><br>
 Task Management<br>
- For employees, navigate to the "Assigned Tasks" section to view and manage your tasks.<br>
- Supervisors and Managers can utilize the dashboard to assign tasks or activities, manage employees, and generate reports.<br>
